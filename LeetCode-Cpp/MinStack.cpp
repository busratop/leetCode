class MinStack {
    private:
    stack<int> dataStack; // Stack to store actual elements
    stack<int> minStack;  // Stack to store minimum elements
public:
    MinStack() {
        
    }
    
    void push(int val) {
        dataStack.push(val);
        if (minStack.empty() || val <= minStack.top()) {
            minStack.push(val);
        }
    }
    
    void pop() {
        if (dataStack.top() == minStack.top()) {
            minStack.pop();
        }
        dataStack.pop();
    }
    
    int top() {
        return dataStack.top();
    }
    
    int getMin() {
        return minStack.top();
    }
};

